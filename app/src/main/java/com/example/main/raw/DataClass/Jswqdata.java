package com.example.main.raw.DataClass;

public class Jswqdata {
   String img;
    String name;
    int start;
    int lx;

    public Jswqdata(String img, String name, int start, int lx) {
        this.img = img;
        this.name = name;
        this.start = start;
        this.lx = lx;
    }

    public String getImg() {
        return img;
    }

    public String getName() {
        return name;
    }

    public int getStart() {
        return start;
    }

    public int getLx() {
        return lx;
    }
}
